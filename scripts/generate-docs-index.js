#!/usr/bin/env node

/**
 * generate-docs-index.js
 * 
 * Script to scan project directories for markdown files and add them as links to Docs/index.md
 * 
 * Usage:
 *   node generate-docs-index.js [depth]
 */

const fs = require('fs');
const path = require('path');

// Default values
const DEFAULT_DEPTH = 3;
const MAX_DEPTH = process.argv[2] ? parseInt(process.argv[2], 10) : DEFAULT_DEPTH;
const INDEX_FILE = path.resolve(process.cwd(), 'Docs/index.md');
const EXCLUDE_DIRS = ['node_modules', '.git', 'dist', 'build', '.github', 'coverage'];
const EXCLUDE_FILES = ['.DS_Store', '.gitignore', 'package-lock.json'];
const FILE_EXTENSIONS = ['.md']; // Only markdown files

// Help text
function showHelp() {
  console.log(`Usage: node ${path.basename(__filename)} [depth]`);
  console.log();
  console.log('Scans project directories for markdown files and adds them as links to Docs/index.md');
  console.log();
  console.log('Arguments:');
  console.log(`  depth    Maximum directory depth to search (default: ${DEFAULT_DEPTH})`);
  console.log();
  console.log('Examples:');
  console.log(`  node ${path.basename(__filename)}         # Search with default depth of ${DEFAULT_DEPTH}`);
  console.log(`  node ${path.basename(__filename)} 5       # Search with a maximum depth of 5`);
  console.log();
}

// Show help if requested
if (process.argv[2] === '--help' || process.argv[2] === '-h') {
  showHelp();
  process.exit(0);
}

// Validate depth argument
if (process.argv[2] && (isNaN(MAX_DEPTH) || MAX_DEPTH < 1)) {
  console.error('Error: Depth must be a positive number');
  process.exit(1);
}

// Create Docs directory if it doesn't exist
const docsDir = path.dirname(INDEX_FILE);
try {
  fs.mkdirSync(docsDir, { recursive: true });
} catch (err) {
  if (err.code !== 'EEXIST') {
    console.error(`Error creating Docs directory: ${err.message}`);
    process.exit(1);
  }
}

console.log(`Scanning for markdown files (max depth: ${MAX_DEPTH})...`);

/**
 * Walk directory recursively to find files
 */
function walkDir(dir, depth = 0) {
  if (depth > MAX_DEPTH) return [];
  
  let results = [];
  const list = fs.readdirSync(dir);
  
  for (const file of list) {
    const fullPath = path.join(dir, file);
    const relativePath = path.relative(process.cwd(), fullPath);
    const stat = fs.statSync(fullPath);
    
    // Skip excluded directories and files
    if (stat.isDirectory()) {
      if (!EXCLUDE_DIRS.includes(file)) {
        results = results.concat(walkDir(fullPath, depth + 1));
      }
    } else if (!EXCLUDE_FILES.includes(file)) {
      const ext = path.extname(file).toLowerCase();
      if (FILE_EXTENSIONS.includes(ext)) {
        results.push({
          path: relativePath,
          name: file,
          directory: path.dirname(relativePath),
          extension: ext,
          size: stat.size,
          mtime: stat.mtime
        });
      }
    }
  }
  
  return results;
}

/**
 * Generate the index.md file content
 */
function generateIndexContent(files) {
  const now = new Date();
  const dateStr = now.toISOString().split('T')[0];
  
  let content = `# Agentis Project Documentation Index\n\n`;
  content += `> This file is automatically generated by the \`generate-docs-index.js\` script.\n`;
  content += `> Last updated: ${dateStr}\n\n`;
  content += `This index provides links to markdown documentation files in the Agentis project. To update this index, run:\n\n`;
  content += `\`\`\`bash\nnode scripts/generate-docs-index.js\n\`\`\`\n\n`;
  content += `You can specify a different search depth (default is ${DEFAULT_DEPTH}):\n\n`;
  content += `\`\`\`bash\nnode scripts/generate-docs-index.js 4\n\`\`\`\n\n`;
  content += `## Documentation Files\n\n`;

  // Group files by directory
  const filesByDir = {};
  
  files.forEach(file => {
    if (!filesByDir[file.directory]) {
      filesByDir[file.directory] = [];
    }
    filesByDir[file.directory].push(file);
  });

  // Sort directories
  const sortedDirs = Object.keys(filesByDir).sort();
  
  // Write directory sections
  sortedDirs.forEach(dir => {
    const dirName = dir === '.' ? 'Root Directory' : dir;
    content += `### ${dirName}\n\n`;
    
    // Sort files in this directory by name
    const sortedFiles = filesByDir[dir].sort((a, b) => a.name.localeCompare(b.name));
    
    sortedFiles.forEach(file => {
      content += `- [${file.name}](/${file.path})\n`;
    });
    
    content += '\n';
  });

  // Add a summary
  content += `## Summary\n\n`;
  content += `- Total markdown files indexed: ${files.length}\n`;
  content += `- Directories with markdown files: ${sortedDirs.length}\n`;
  content += `- Scan depth: ${MAX_DEPTH}\n\n`;
  content += `> Note: Some directories are excluded from this index.\n`;

  return content;
}

try {
  // Find all files
  const files = walkDir(process.cwd());
  
  console.log(`Found ${files.length} markdown files in ${Object.keys(files.reduce((acc, file) => {
    acc[file.directory] = true;
    return acc;
  }, {})).length} directories.`);
  
  // Generate index content
  const indexContent = generateIndexContent(files);
  
  // Write to index.md
  fs.writeFileSync(INDEX_FILE, indexContent);
  
  console.log(`Done! Markdown index written to ${INDEX_FILE}`);
} catch (err) {
  console.error(`Error: ${err.message}`);
  process.exit(1);
}