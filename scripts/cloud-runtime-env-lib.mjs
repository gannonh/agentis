/**
 * Shared helpers for cloud-agent runtime env bootstrap.
 * Cursor injects GH_TOKEN reliably; runtime secrets often are not injected
 * (see forum.cursor.com/t/163026). We fall back to GitHub Actions variables
 * prefixed with AGENTIS_RUNTIME_.
 */

export const RUNTIME_ENV_PREFIX = "AGENTIS_RUNTIME_"

/** Env vars required for live Agentis runtime (golden-path defaults). */
export const LIVE_RUNTIME_ENV_NAMES = [
  "AI_GATEWAY_PROVIDER",
  "CLOUDFLARE_API_KEY",
  "CLOUDFLARE_ACCOUNT_ID",
  "COMPOSIO_API_KEY",
  "AGENTIS_MOCK_RUNTIME",
  "AGENTIS_MOCK_COMPOSIO",
]

/** Optional but commonly needed alongside live runtime. */
export const OPTIONAL_RUNTIME_ENV_NAMES = [
  "CLOUDFLARE_AI_GATEWAY_ID",
  "VERCEL_AI_GATEWAY_API_KEY",
  "AI_GATEWAY_API_KEY",
  "COMPOSIO_REDIRECT_BASE_URL",
  "AGENTIS_WEB_SEARCH_PROVIDER",
  "AGENTIS_WEB_SEARCH_BACKEND",
]

export function isCloudAgentEnv(env = process.env) {
  return env.CURSOR_AGENT === "1"
}

export function parseInjectedSecretNames(env = process.env) {
  const raw =
    env.CLOUD_AGENT_INJECTED_SECRET_NAMES ?? env.CLOUD_AGENT_ALL_SECRET_NAMES
  if (!raw?.trim()) return []
  return raw
    .split(",")
    .map((name) => name.trim())
    .filter(Boolean)
}

export function runtimeVariableName(envName) {
  return `${RUNTIME_ENV_PREFIX}${envName}`
}

export function envNameFromRuntimeVariable(variableName) {
  if (!variableName.startsWith(RUNTIME_ENV_PREFIX)) return null
  const envName = variableName.slice(RUNTIME_ENV_PREFIX.length)
  return envName.length > 0 ? envName : null
}

export function isSetEnvValue(value) {
  return typeof value === "string" && value.trim().length > 0
}

export function missingEnvNames(env, names) {
  return names.filter((name) => !isSetEnvValue(env[name]))
}

export function redactValue(value) {
  if (!isSetEnvValue(value)) return "(unset)"
  if (value.length <= 8) return "***"
  return `${value.slice(0, 4)}…${value.slice(-4)}`
}

export function parseGitHubRepository(env = process.env) {
  const fromEnv = env.GITHUB_REPOSITORY?.trim()
  if (fromEnv?.includes("/")) {
    const [owner, repo] = fromEnv.split("/", 2).map((part) => part.trim())
    return { owner, repo }
  }
  return null
}

export async function detectGitHubRepository(execFile) {
  const fromEnv = parseGitHubRepository()
  if (fromEnv) return fromEnv

  const remote = await execFile("git", [
    "remote",
    "get-url",
    "origin",
  ]).catch(() => ({ stdout: "" }))
  const match = String(remote.stdout).trim().match(
    /github\.com[:/](?<owner>[^/]+)\/(?<repo>[^/.]+)/
  )
  if (!match?.groups) return null
  return {
    owner: match.groups.owner.trim(),
    repo: match.groups.repo.trim(),
  }
}

export async function ghApiJson(path, { token, execFile }) {
  const result = await execFile("gh", [
    "api",
    path,
    "--header",
    `Authorization: Bearer ${token}`,
  ])
  return JSON.parse(result.stdout.trim())
}

export async function fetchRuntimeVariables({ owner, repo, token, execFile }) {
  const variables = []
  let page = 1
  while (true) {
    const body = await ghApiJson(
      `/repos/${owner}/${repo}/actions/variables?per_page=100&page=${page}`,
      { token, execFile }
    )
    const batch = body.variables ?? []
    variables.push(
      ...batch.filter((entry) => entry.name?.startsWith(RUNTIME_ENV_PREFIX))
    )
    if (batch.length < 100) break
    page += 1
  }
  return variables
}

export function materializeEnvFromVariables(variables, env = process.env) {
  const materialized = {}
  for (const variable of variables) {
    const envName = envNameFromRuntimeVariable(variable.name)
    if (!envName || !isSetEnvValue(variable.value)) continue
    if (isSetEnvValue(env[envName])) continue
    materialized[envName] = variable.value.trim()
  }
  return materialized
}

export function formatEnvFile(entries) {
  const lines = [
    "# Generated by scripts/materialize-cloud-runtime-env.mjs",
    "# Do not commit. Cursor Cloud fallback when runtime secrets are not injected.",
    "",
  ]
  for (const [key, value] of Object.entries(entries).sort(([a], [b]) =>
    a.localeCompare(b)
  )) {
    lines.push(`${key}=${escapeEnvValue(value)}`)
  }
  lines.push("")
  return lines.join("\n")
}

function escapeEnvValue(value) {
  if (/^[A-Za-z0-9_./:@-]+$/.test(value)) return value
  return `"${value.replace(/\\/g, "\\\\").replace(/"/g, '\\"').replace(/\n/g, "\\n")}"`
}

export function cursorInjectionDiagnosis(env = process.env) {
  const injected = parseInjectedSecretNames(env)
  const hasMarkers =
    env.CLOUD_AGENT_INJECTED_SECRET_NAMES !== undefined ||
    env.CLOUD_AGENT_ALL_SECRET_NAMES !== undefined
  const runtimeSecretsExpected = LIVE_RUNTIME_ENV_NAMES.filter((name) =>
    /KEY|TOKEN|SECRET/i.test(name)
  )
  const runtimeSecretsInjected = runtimeSecretsExpected.filter((name) =>
    injected.includes(name)
  )

  if (!isCloudAgentEnv(env)) {
    return {
      mode: "local",
      message: "Not a Cursor cloud agent shell.",
    }
  }

  if (!hasMarkers) {
    return {
      mode: "cursor-injection-missing",
      message:
        "Cursor secret injection markers are unset. This matches the automation-launched cloud agent bug (forum.cursor.com/t/163026). Using GitHub variable fallback when configured.",
      injected,
    }
  }

  if (runtimeSecretsInjected.length === 0) {
    return {
      mode: "cursor-injection-partial",
      message:
        `Cursor injected ${injected.length} name(s) but none are live runtime credentials (${runtimeSecretsExpected.join(", ")}). Using GitHub variable fallback when configured.`,
      injected,
    }
  }

  return {
    mode: "cursor-injection-ok",
    message: `Cursor injected runtime credentials: ${runtimeSecretsInjected.join(", ")}.`,
    injected,
  }
}
